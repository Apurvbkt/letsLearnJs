class users{
    constructor(email, password){
        this.email = email;
        this.password = password
    }
    
    get email(){
        return this.email.toUpperCase()
    }
    
    set email(){
        return this.email.toUpperCase()
    }
    
    get password(){
        return this.email.toUpperCase()
    }
    set password(){
        return this.password.toUpperCase()
    }
}

